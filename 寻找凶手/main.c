//题目内容
//日本某地发生了一件谋杀案，警察通过排查确定杀人凶手必为4个嫌疑犯的一个
//以下为4个嫌疑犯的供词
//A说 : 不是我。//1
//B说 : 是C。//1
//c说 : 是D。//0
//D说 : C在胡说  //1
//1已知3个人说了真话，1个人说的是假话。
//现在请根据这些信息，写一个程序来确定到底谁是凶手

int main()
{
	
	//test();

	int killer = 0;

	for (killer = 'a'; killer <= 'd'; killer++)
	{
		if ((killer != 'a') + (killer == 'c') + (killer == 'd') + (killer != 'd') == 3)
		{
			printf("凶手是:%c", killer);
		}
	}

	return 0;
}